function showToast(message, type = 'success') {
    let container = document.getElementById('toast-container');
    if (!container) {
        container = document.createElement('div');
        container.id = 'toast-container';
        document.body.appendChild(container);
    }

    const toast = document.createElement('div');
    toast.className = `toast ${type}`;
    toast.innerHTML = `
        <span class="toast-icon">${type === 'success' ? '✔' : '✕'}</span>
        <span class="toast-text">${message}</span>
    `;

    container.appendChild(toast);

    // Kích hoạt animation tắt sau 3 giây
    setTimeout(() => {
        toast.classList.add('fade-out');
        
        // Cứu cánh bằng setTimeout (an toàn tuyệt đối nếu CSS transition bị lỗi)
        setTimeout(() => {
            toast.remove();
        }, 300); 
    }, 3000);
}

function escHtml(s) {
    return String(s).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');
}
function toggleGlobalLoader(show) {
    const loader = document.getElementById('global-loader');
    if (loader) {
        loader.style.display = show ? 'flex' : 'none';
    }
}
async function fetchWishes() {
    const listLoader = document.getElementById('list-loader');
    const wishesList = document.getElementById('wishes-list');


    if (listLoader) listLoader.style.display = 'flex';
    if (wishesList) wishesList.style.opacity = '0.4';

    try {
        const response = await fetch(`${SCRIPT_URL}?action=getWishes`, {
            method: 'GET',
            redirect: 'follow'
        });
        const result = await response.json();
        if (result && result.status === 'success') {
            wishes = result.data;
            renderWishes();
        }
    } catch (err) {
        console.error("Error fetching data from Google Sheets:", err);
    } finally {
        if (listLoader) listLoader.style.display = 'none';
        if (wishesList) wishesList.style.opacity = '1';
    }
}

function renderWishes() {
    const list = document.getElementById('wishes-list');
    if (!list) {
        return;
    }
    
    const t = i18n[lang];
    if (!wishes || wishes.length === 0) {
        list.innerHTML = `<p class="empty-wishes">${t.empty_wishes}</p>`;
        return;
    }
    
    list.innerHTML = wishes.map(w => {
        let dateVal = '';
        if (w.created_at) {
            const parsedDate = new Date(w.created_at);
            dateVal = isNaN(parsedDate.getTime()) ? String(w.created_at).split('T')[0] : parsedDate.toLocaleDateString('en-GB');
        }

        const relationStr = w.relationship ? ` (${w.relationship})` : '';

        
        return `
            <div class="wish-card">
                <div class="wish-header">
                    <span class="wish-name">
                        ${escHtml(w.name || '')}
                        <small style="opacity:0.7; font-weight:normal;">${escHtml(relationStr)}</small>
                    </span>
                    <span class="wish-time">${escHtml(dateVal)}</span>
                </div>
                <p class="wish-text">${escHtml(w.message || '')}</p>
            </div>
        `;
    }).join('');
}

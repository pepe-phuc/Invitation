function initSubmitForm() {
    const submitBtn = document.getElementById('submit-wish');
    if (!submitBtn) {
        return;
    }
    submitBtn.addEventListener('click', async () => {
        
        const nameEl = document.getElementById('wish-name');
        const msgEl = document.getElementById('wish-msg');
        const relaEl = document.getElementById('wish-relation');
        const attendanceEl = document.querySelector('input[name="attendance"]:checked');
        
        if (!nameEl || !msgEl) {
            return;
        }

        const name = nameEl.value.trim();
        const msg = msgEl.value.trim();
        const relation = relaEl ? relaEl.value.trim() : '';
        const attendance = attendanceEl ? attendanceEl.value : 'yes';
        const t = i18n[lang];
        
        if (!name || !msg) {
            showToast(t.toast_err);
            return;
        }

        submitBtn.disabled = true;
        toggleGlobalLoader(true);
        try {
            const response = await fetch(SCRIPT_URL, {
                method: 'POST',
                mode: 'no-cors',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({ name, message: msg, relationship: relation, attendance })
            });

            nameEl.value = '';
            msgEl.value = '';
            if (relaEl) {
                relaEl.value = '';
            }
            
            showToast(t.toast_ok);
            setTimeout(() => {
                fetchWishes();
                submitBtn.disabled = false;
            }, 1000);
        } catch (err) {
            showToast("Có lỗi xảy ra, vui lòng thử lại.");
            submitBtn.disabled = false;
        } finally {
            toggleGlobalLoader(false);
        }
    });
}

function initCountdown() {
    const targetDate = new Date('2026-07-05T10:30:00').getTime();
    const dEl = document.getElementById('days');
    const hEl = document.getElementById('hours');
    const mEl = document.getElementById('minutes');

    const updateCountdown = () => {
        const diff = targetDate - Date.now();
        if (diff <= 0) {
            clearInterval(countdownInterval);
            return;
        }
        const d = Math.floor(diff / (1000 * 60 * 60 * 24));
        const h = Math.floor((diff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
        const m = Math.floor((diff % (1000 * 60 * 60)) / (1000 * 60));

        if (dEl) { dEl.textContent = String(d).padStart(2, '0'); }
        if (hEl) { hEl.textContent = String(h).padStart(2, '0'); }
        if (mEl) { mEl.textContent = String(m).padStart(2, '0'); }
    };
    const countdownInterval = setInterval(updateCountdown, 1000);
    updateCountdown();
}

function initGalleryLightbox() {
    const lightbox = document.getElementById('gallery-lightbox');
    const lightboxImg = document.getElementById('lightbox-img');
    const closeBtn = document.querySelector('.lightbox-close');
    const galleryImages = document.querySelectorAll('.gallery-item img');

    if (!lightbox || !lightboxImg) return;

    // Bắt sự kiện click vào từng ảnh trong grid
    galleryImages.forEach(img => {
        img.parentElement.addEventListener('click', () => {
            lightboxImg.src = img.src;
            lightbox.style.display = 'flex';
            setTimeout(() => lightbox.classList.add('show'), 10);
        });
    });

    // Đóng khi bấm nút X
    if (closeBtn) {
        closeBtn.addEventListener('click', closeLightbox);
    }

    // Đóng khi bấm vùng ngoài tầm ảnh
    lightbox.addEventListener('click', (e) => {
        if (e.target === lightbox) {
            closeLightbox();
        }
    });

    function closeLightbox() {
        lightbox.classList.remove('show');
        setTimeout(() => {
            lightbox.style.display = 'none';
            lightboxImg.src = '';
        }, 300);
    }
}
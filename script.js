function initGradCapAnimation() {
    setInterval(() => {
        const body = document.body;
        if (!body) {
            return;
        }
        const count = 15;
        for (let i = 0; i < count; i++) {
            setTimeout(() => {
                const cap = document.createElement('div');
                cap.classList.add('grad-cap-global');
                cap.innerText = '🎓';
                cap.style.left = (Math.random() * 90 + 5) + 'vw';
                cap.style.fontSize = (Math.random() * 20 + 24) + 'px';
                body.appendChild(cap);

                const xMove = (Math.random() - 0.5) * 200;
                const rotate = (Math.random() - 0.5) * 1080;

                requestAnimationFrame(() => {
                    cap.style.transform = `translate(${xMove}px, -110vh) rotate(${rotate}deg)`;
                });

                setTimeout(() => {
                    cap.style.opacity = '0';
                    setTimeout(() => {
                        cap.remove();
                    }, 3000);
                }, 4000);
            }, i * 150);
        }
    }, 10000);
}

function initAudioControls() {
    const bgMusic = document.getElementById('bgMusic') || document.getElementById('bg-music');
    const musicToggle = document.getElementById('musicToggle') || document.getElementById('music-btn');
    const welcomeScreen = document.getElementById('welcome-screen');
    const btnOpenWedding = document.getElementById('btn-open-wedding');
    let isPlaying = false;

    if (bgMusic) {
        bgMusic.volume = 0.3;
    }

    const playAudio = () => {
        if (!bgMusic) {
            return;
        }
        bgMusic.play().then(() => {
            isPlaying = true;
            if (musicToggle) {
                musicToggle.classList.add('playing');
                musicToggle.classList.remove('manually-paused');
            }
        }).catch(() => {});
    };

    const pauseAudio = () => {
        if (!bgMusic) {
            return;
        }
        bgMusic.pause();
        isPlaying = false;
        if (musicToggle) {
            musicToggle.classList.remove('playing');
            musicToggle.classList.add('manually-paused');
        }
    };

    if (musicToggle && bgMusic) {
        musicToggle.addEventListener('click', () => {
            if (bgMusic.paused) {
                playAudio();
            } else {
                pauseAudio();
            }
        });
    }

    if (welcomeScreen && btnOpenWedding) {
        btnOpenWedding.addEventListener('click', () => {
            welcomeScreen.classList.add('opened');
            playAudio();
        });
    }

    document.addEventListener('click', () => {
        if (!isPlaying && bgMusic && bgMusic.paused) {
            playAudio();
        }
    }, { once: true });
}

function initIntersectionObserver() {
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const idx = [...sections].indexOf(entry.target);
                dots.forEach((d, i) => {
                    d.classList.toggle('active', i === idx);
                });
                entry.target.querySelectorAll('.reveal').forEach(el => {
                    el.classList.add('visible');
                });
            }
        });
    }, { threshold: 0.3 });

    sections.forEach(s => {
        observer.observe(s);
    });

    dots.forEach(dot => {
        dot.addEventListener('click', () => {
            sections[+dot.dataset.s].scrollIntoView({ behavior: 'smooth' });
        });
    });
}

